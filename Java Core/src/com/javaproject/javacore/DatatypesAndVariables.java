package com.javaproject.javacore;
public class DatatypesAndVariables {
    public static void main(String[] args) {
        int i = 10;
        long l = 10l;
        float f = 10f;
        double d = 10;
        char c = 'j';
        boolean x = true;
        System.out.println("i: "+i+" l: "+l+" f: "+f+" d: "+d+" c: "+c+" b: "+x);
    }    
}
