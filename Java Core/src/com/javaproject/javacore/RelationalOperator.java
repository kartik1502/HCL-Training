package com.javaproject.javacore;
public class RelationalOperator {
    public static void main(String[] args) {
        int a = 10;
        int b = 20;
        boolean c = a > b;
        boolean d = b >= a;
        System.out.println(c);
        System.out.println(d);
        System.out.println(a > b);
        System.out.println(a < b);
        System.out.println(a >= b);
        System.out.println(a <= b);
        System.out.println(a == b);
        System.out.println(a != b);
    }
}
